void testEEPROM() {
	//
	uint16_t len = 128;
	uint8_t data[len];
	uint8_t result[len];
	uint16_t i;
	for (i = 0; i < len; i++)
		data[i] = 0xFF & i;
	EEPROMwrite(0x00, &data[0], len);
	while (EEPROMbusy())
		;
	HAL_Delay(20);
	EEPROMread(0x00, &result[0], len);
	while (EEPROMbusy())
		;
	HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
	for (i = 0; i < len; i++) {
		if (data != result)
			HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
	}
}

